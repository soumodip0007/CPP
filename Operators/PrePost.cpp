#include <iostream>
using namespace std;

int main() {
    int a = 10;
    cout << a << endl;
    cout << "Pre & Post - Increment" << endl;
    cout << a++ << endl;
    cout << ++a << endl;

    cout << a << endl;
    cout << "Pre & Post - Decrement" << endl;
    cout << a-- << endl;
    cout << --a << endl;

    return 0;
}

