#include <iostream>
using namespace std;

int main() {
    int a;
    a = 10; //simple
    cout << a << endl;

    a += 10; //compound
    cout << a << endl;
 
    a -= 10; //compound
    cout << a << endl;

    a *= 10; //compound
    cout << a << endl;

    a /= 10; //compound
    cout << a << endl;

    return 0;
}

